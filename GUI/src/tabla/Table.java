    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel modeloTabla=new DefaultTableModel();
        TablaLider.setModel(modeloTabla);
        Connection conn=null;
        PreparedStatement stmt=null;
        ResultSet rs=null;
         String SQL_SELECT="select ID_Lider,Nombre,Primer_Apellido,Ciudad_Residencia from Lider order by Ciudad_Residencia Asc ";
        try{
        conn=JDBCUtilities.getConnection();
        stmt=conn.prepareStatement(SQL_SELECT);
        rs=stmt.executeQuery();
        modeloTabla.addColumn("ID_Lider");
        modeloTabla.addColumn("Nombre");
        modeloTabla.addColumn("Primer_Apellido");
        modeloTabla.addColumn("Ciudad_Residencia");
        
            while (rs.next()) {  
                Object fila[] = new Object[4];
                for(int i=0;i<4;i++){
                    fila[i]=rs.getObject(i+1);
                }
                modeloTabla.addRow(fila);
            }
        
    }catch (SQLException ex) {  
            ex.printStackTrace(System.out);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

